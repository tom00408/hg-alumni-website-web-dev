<template>
    <div class="impressum">
      <div class="content-container">
        <HgCard class="impressum-card">
          <h1 class="page-title">Satzung</h1>
          
          <div class="satzung-content">
            <div class="satzung-header">
              <p class="satzung-subtitle">Alumniverein des Hainberg-Gymnasiums Göttingen e.V.</p>
              <p class="satzung-date">Stand 26.11.2013 mit Änderung vom 12.12.2014</p>
            </div>

            <section class="satzung-section">
              <h2>§ 1 Name und Sitz des Vereins, Geschäftsjahr</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Der Verein führt den Namen „Alumniverein des Hainberg-Gymnasiums Göttingen". Er soll in das Vereinsregister eingetragen werden und danach den Zusatz „e.V." führen. Als Kurzform soll „Alumniverein HG" benutzt werden.</p>
                <p><strong>(2)</strong> Der Verein hat seinen Sitz in Göttingen.</p>
                <p><strong>(3)</strong> Das Geschäftsjahr ist das Kalenderjahr.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 2 Zweck, Aufgaben, Gemeinnützigkeit des Vereins</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Der Alumniverein HG verfolgt ausschließlich und unmittelbar gemeinnützige Zwecke im Sinne des Abschnitts „Steuerbegünstigte Zwecke" der Abgabenordnung.</p>
                <p><strong>(2)</strong> Zweck des Vereins ist die Förderung der Erziehung, Volks- und Berufsbildung einschließlich der Studentenhilfe sowie von Kunst und Kultur. Dieser Zweck wird insbesondere verwirklicht durch</p>
                <ul>
                  <li>Die Durchführung von und die Mittelbeschaffung für Bildungs-, Kunst und Kulturveranstaltungen am Hainberg-Gymnasium sowie durch</li>
                  <li>Die Kontaktpflege zwischen den Schülern und den Alumni des Hainbergs-Gymnasiums mit dem Ziel der Beratung bei der Wahl und der Vermittlung von Studien-, Praktikums-, Ausbildungsplätzen u.ä.</li>
                </ul>
                <p><strong>(3)</strong> Der Verein ist selbstlos tätig; er verfolgt nicht in erster Linie eigenwirtschaftliche Zwecke.</p>
                <p><strong>(4)</strong> Mittel des Vereins dürfen nur für die satzungsmäßigen Zwecke verwendet werden. Die Mitglieder erhalten keine Zuwendungen aus den Mitteln des Vereins. Keine Person darf durch unverhältnismäßig hohe Vergütungen oder durch Ausgaben, die dem Zweck des Vereins fremd sind, begünstigt werden.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 3 Erwerb der Mitgliedschaft</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Mitglied des Vereins kann jede natürliche Person nach der Vollendung des 16. Lebensjahres werden.</p>
                <p><strong>(2)</strong> Die Aufnahme in den Verein ist beim Vorstand in Textform zu beantragen. Der Vorstand entscheidet über den Aufnahmeantrag. Will er dem Antrag nicht stattgeben, entscheidet hierüber die nächste ordentliche Mitgliederversammlung.</p>
                <p><strong>(3)</strong> Auf Vorschlag des Vorstands kann die Mitgliederversammlung besonders verdienstvolle Mitglieder zu Ehrenmitgliedern auf Lebenszeit ernennen.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 4 Beendigung der Mitgliedschaft</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Die Mitgliedschaft im Verein endet durch Tod, Austritt oder Ausschluss.</p>
                <p><strong>(2)</strong> Der Austritt ist gegenüber dem Vorstand in Textform zu erklären. Der Austritt kann nur mit einer Frist von zwei Monaten zum Ende des Geschäftsjahres erklärt werden.</p>
                <p><strong>(3)</strong> Ein Mitglied kann durch Beschluss der Mitgliederversammlung aus dem Verein ausgeschlossen werden, wenn es</p>
                <ul>
                  <li>schuldhaft das Ansehen oder die Interessen des Vereins in schwerwiegender Weise geschädigt oder</li>
                  <li>die ihm nach der Satzung obliegenden Pflichten wiederholt verletzt hat oder</li>
                </ul>
                <p>Dem Mitglied ist Gelegenheit zu geben, in der Mitgliederversammlung zu den Gründen des Ausschlusses Stellung zu nehmen. Diese sind ihm mindestens zwei Wochen vorher mitzuteilen.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 5 Rechte und Pflichten der Mitglieder</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Jedes Mitglied hat das Recht, aktiv mitzuwirken und an gemeinsamen Veranstaltungen teilzunehmen. Jedes Mitglied hat gleiches Stimm- und Wahlrecht in der Mitgliederversammlung.</p>
                <p><strong>(2)</strong> Jedes Mitglied hat die Pflicht, die Interessen des Vereins zu fördern, insbesondere regelmäßig seine Mitgliedsbeiträge zu leisten und soweit es in seinen Kräften steht, die Veranstaltungen des Vereins durch seine Mitarbeit zu unterstützen.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 6 Mitgliedsbeiträge</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Jedes Mitglied hat einen jährlich fällig werdenden Mitgliedsbeitrag zu entrichten. Über die Höhe und Fälligkeit entscheidet die Mitgliederversammlung. Dabei sind Alter und die wirtschaftliche Leistungsfähigkeit der Mitglieder angemessen zu berücksichtigen.</p>
                <p><strong>(2)</strong> Ehrenmitglieder sind von dem Mitgliedsbeitrag befreit.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 7 Organe des Vereins</h2>
              <div class="paragraph-content">
                <p>Organe des Vereins sind der Vorstand und die Mitgliederversammlung.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 8 Vorstand</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Dem Vorstand des Vereins obliegen die Vertretung des Vereins nach § 26 BGB und die Führung seiner Geschäfte. Er hat insbesondere folgende Aufgaben:</p>
                <ul>
                  <li>die Einberufung und Vorbereitung der Mitgliederversammlungen einschließlich der Aufstellung der Tagesordnung,</li>
                  <li>die Ausführung von Beschlüssen der Mitgliederversammlung,</li>
                  <li>die Verwaltung des Vereinsvermögens und die Anfertigung des Jahresberichts,</li>
                  <li>die Aufnahme neuer Mitglieder.</li>
                </ul>
                <p><strong>(2)</strong> Der Vorstand besteht aus</p>
                <ul>
                  <li>zwei Vorsitzenden,</li>
                  <li>dem Schatzmeister,</li>
                  <li>einem Vorstandsmitglied für Öffentlichkeitsarbeit und Mitgliederbetreuung und</li>
                  <li>bis zu drei vom Vorstand berufenen Mitgliedern</li>
                </ul>
                <p>Ferner nimmt der Schulleiter des Hainberg-Gymnasiums Göttingen an den Vorstandssitzungen mit beratender Stimme teil.</p>
                <p><strong>(3)</strong> Alle Vorstandsmitglieder sind einzeln vertretungsbefugt.</p>
                <p><strong>(4)</strong> Der Vorstand gibt sich eine Geschäftsordnung. Diese regelt insbesondere</p>
                <ul>
                  <li>die Zuständigkeitsbereiche der Vorstandsmitglieder,</li>
                  <li>die Beschlussfassung im Vorstand und</li>
                  <li>Näheres zu den Vorstandssitzungen.</li>
                </ul>
                <p><strong>(5)</strong> Die Mitglieder des Vorstandes werden von der Mitgliederversammlung für die Dauer von zwei Jahren einzeln mit der Mehrheit der anwesenden Mitglieder gewählt. Mitglieder des Vorstands können nur Mitglieder des Vereins sein; mit der Mitgliedschaft im Verein endet auch die Mitgliedschaft im Vorstand. Die Wiederwahl oder die vorzeitige Abberufung eines Mitglieds durch die Mitgliederversammlung ist zulässig. Ein Mitglied bleibt nach Ablauf der regulären Amtszeit bis zur Wahl seines Nachfolgers im Amt. Scheidet ein Mitglied vorzeitig aus dem Vorstand aus, so sind die verbleibenden Mitglieder berechtigt, ein Mitglied des Vereins bis zur Wahl des Nachfolgers durch die Mitgliederversammlung in den Vorstand zu wählen.</p>
                <p><strong>(6)</strong> Die Beschlüsse des Vorstandes sind zu protokollieren. Das Protokoll ist vom Protokollführer sowie einem Vorsitzenden zu unterschreiben.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 9 Mitgliederversammlung</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Die Mitgliederversammlung ist zuständig für die Entscheidungen in folgenden Angelegenheiten:</p>
                <ul>
                  <li>Änderungen der Satzung,</li>
                  <li>die Auflösung des Vereins,</li>
                  <li>die Aufnahme neuer Vereinsmitglieder in den Fällen des § 3 Abs. 2 Satz 3, die Ernennung von Ehrenmitgliedern sowie den Ausschluss von Mitgliedern aus dem Verein,</li>
                  <li>die Wahl und die Abberufung der Mitglieder des Vorstands,</li>
                  <li>die Wahl und die Abberufung zweier Kassenprüfer,</li>
                  <li>die Entgegennahme des Jahresberichts und die Entlastung des Vorstands,</li>
                  <li>die Festsetzung der Mitgliedsbeiträge.</li>
                </ul>
                <p><strong>(2)</strong> Mindestens einmal im Jahr, möglichst im ersten Quartal, ist vom Vorstand eine ordentliche Mitgliederversammlung einzuberufen. Die Einberufung erfolgt in Textform unter Einhaltung einer Frist von einem Monat und unter Angabe der Tagesordnung.</p>
                <p><strong>(3)</strong> Die Tagesordnung setzt der Vorstand fest. Jedes Vereinsmitglied kann bis spätestens eine Woche vor der Mitgliederversammlung beim Vorstand in Textform eine Ergänzung der Tagesordnung beantragen. Über den Antrag entscheidet der Vorstand. Über Anträge zur Tagesordnung, die vom Vorstand nicht aufgenommen wurden oder die erstmals in der Mitgliederversammlung gestellt werden, entscheidet die Mitgliederversammlung mit der Mehrheit der Stimmen der anwesenden Mitglieder; dies gilt nicht für Anträge, die eine Änderung der Satzung, die Auflösung des Vereins oder Änderungen der Mitgliedsbeiträge zum Gegenstand haben.</p>
                <p><strong>(4)</strong> Der Vorstand hat eine außerordentliche Mitgliederversammlung einzuberufen, wenn es das Interesse des Vereins erfordert oder wenn ein Viertel der Mitglieder dies in Textform unter Angabe des Zwecks und der Gründe beantragt. Soweit die Umstände dies zulassen, ist eine Ladungsfrist von einem Monat einzuhalten und die Tagesordnung mit der Einladung bekannt zu geben.</p>
                <p><strong>(5)</strong> Den Versammlungsleiter bestimmt der Vorstand aus seiner Mitte.</p>
                <p><strong>(6)</strong> Die Mitgliederversammlung ist beschlussfähig, wenn sie ordnungsgemäß einberufen wurde, und mindestens aber fünf Mitglieder anwesend sind.</p>
                <p><strong>(7)</strong> Die Mitgliederversammlung beschließt in offener Abstimmung mit der Mehrheit der Stimmen der anwesenden Mitglieder. Kann bei Wahlen kein Kandidat die Mehrheit der Stimmen der anwesenden Mitglieder auf sich vereinen, ist gewählt, wer die Mehrheit der abgegebenen gültigen Stimmen erhalten hat. Handelt es sich nicht um eine Personalentscheidung, zählt bei Stimmengleichheit die Stimme des Versammlungsleiters doppelt. Beschlüsse über eine Änderung der Satzung bedürfen der Mehrheit von drei Vierteln, der Beschluss über die Auflösung des Vereins der Zustimmung von neun Zehnteln der anwesenden Mitglieder.</p>
                <p><strong>(8)</strong> Über den Ablauf der Mitgliederversammlung und die gefassten Beschlüsse ist ein Protokoll zu fertigen. Dieses ist vom Protokollführer und vom Versammlungsleiter zu unterschreiben und an die Mitglieder in geeigneter Form weiterzuleiten.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 10 Kassenprüfer und Kassenprüfung</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Die Kassenprüfer werden gemäß § 9 Abs. 1 Nr. 5 von der Mitgliederversammlung gewählt. Ihre Amtszeit beträgt ein Jahr.</p>
                <p><strong>(2)</strong> Die Kassenprüfung erfolgt jährlich vor der Mitgliederversammlung. Über das Ergebnis der Kassenprüfung berichten die Kassenprüfer der Mitgliederversammlung.</p>
              </div>
            </section>

            <section class="satzung-section">
              <h2>§ 11 Auflösung des Vereins, Beendigung aus anderen Gründen, Wegfall steuerbegünstigter Zwecke</h2>
              <div class="paragraph-content">
                <p><strong>(1)</strong> Im Falle der Auflösung des Vereins sind die Vorstandsmitglieder gemeinsam vertretungsberechtigte Liquidatoren, falls die Mitgliederversammlung keine anderen Personen beruft.</p>
                <p><strong>(2)</strong> Bei Auflösung oder Aufhebung des Vereins oder bei Wegfall steuerbegünstigter Zwecke fällt das Vermögen des Vereins an den Verein der Freunde des Hainberg-Gymnasiums Göttingen e.V., der es ausschließlich und unmittelbar für steuerbegünstigte Zwecke zu verwenden hat.</p>
                <p><strong>(3)</strong> Die vorstehenden Bestimmungen gelten entsprechend, wenn der Verein aus einem anderen Grund aufgelöst wird oder seine Rechtsfähigkeit verliert.</p>
                <p><strong>(4)</strong> Sollten eine oder mehrere Bestimmungen dieser Satzung widerrechtlich oder nicht durchsetzbar sein oder werden, bleiben die übrigen Bestimmungen hiervon unberührt.</p>
              </div>
            </section>

            <section class="satzung-section">
              <div class="satzung-footer">
                <p><strong>Göttingen, 12.12.2014</strong></p>
              </div>
            </section>
          </div>
        </HgCard>
      </div>
